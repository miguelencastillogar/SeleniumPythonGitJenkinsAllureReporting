# impresion numeros enteros
print(2)
print(4)

# impresion numeros flotantes
print(2.5)

# Impresion operaciones aritmeticas
print(2 + 3)
print(8 - 4)
print(1 * 4)
print(10 / 3)

# Se expresario como 4 elevado a la 3 y su resultado es 64
print(4 ** 3)
print(3 ** 3)

# La operacion entre parentesis se ejecuta primero, ya
# los parentesis le asignan prioridad.
print((2 + 3) * 4)

# Incremento, tomar en cuenta que en Python no se permite
# variable++ o ++variable. La manera correcta
# es variable += valor"_incremento.
numero = 20
print(numero)
numero += 1
print(numero)

# Operacion entre diferentes tipos de datos
print(4 + 2.9)
